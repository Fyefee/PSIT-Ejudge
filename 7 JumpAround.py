"""JumpAround"""
def main():
    """calculate x variable"""
    num = int(input())
    output_1 = num + 5
    output_2 = num - 17
    output_3 = num * 32
    output_4 = (5 * (num**2)) + (10 * (5 * num)) + 3
    print("%d\n%d\n%d\n%d\n%d"%(num, output_1, output_2, output_3, output_4))

main()
